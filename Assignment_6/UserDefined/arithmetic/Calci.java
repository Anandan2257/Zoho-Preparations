package arithmetic;
public class Calci{
    public int add(int a,int b){
        return a + b;
    }

    public int sub(int a,int b){
        return a - b;
    }

     public int mul(int a,int b){
        return a * b;
    }

    public int mod(int a,int b){
        return a % b;
    }
    public int division(int a,int b){
        return a / b;
    }
}
